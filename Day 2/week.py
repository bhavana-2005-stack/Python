def week(a):
    if a == 1:
        return "Monday"
    elif a == 2:
        return "Tuesday"
    elif a == 3:
        return "Wednesday"
    elif a == 4:
        return "Thursday"
    elif a == 5:
        return "Friday"
    elif a == 6:
        return "Saturday"
    elif a == 7:
        return "Sunday"
    else:
        return "Invalid input"
week(3)
week(10)
